// 顯示縣市選單
function cityTown() {
  for (var i in cities) {
    $('#city').append(`<option value="${cities[i].name}">${cities[i].name}</option>`);
  }
}

var table;
// 建立table與設定
function showResultTable (answer) {
  table = $('#result').DataTable({
    data: answer,
    searching: false,
    ordering:  false,
    select: false,
    lengthChange: false,
    info: false,
    autoWidth: false,
    pagingType: 'simple_numbers',
    language: {
      paginate: {
        previous: '上一頁',
        next: '下一頁',
      },
      emptyTable: '您查詢的區域尚無推薦資料',
    },
    columns: [
      {data: 'name'},
      {data: 'phone'},
      {data: 'address'},
      {data: 'info'},
    ]
  });
  $('.show_result').show();
}

$(document)
  .ready(function () {
    var answer = [];
    for (var i in hospitals) {
        var item = hospitals[i];
        var recommend = item.recommend == 1 ? ' <span class="recommend">推薦名單</span>' : '';
        answer.push({
            name: item.name + recommend,
            phone: item.phone,
            address: item.address,
            info: item.info,
            recommend: item.recommend
        });
        // 推薦排序提升
        answer.sort(function(a, b) {
          return a.recommend == 1 ? -1 : 1;
        });
    }
    showResultTable(answer);
    cityTown();
    // Smooth scrolling using jQuery easing
    $('a.smooth-scroll[href*="#"]:not([href="#"])').click(function () {
      if (location.pathname.replace(/^\//, '') == this.pathname.replace(/^\//, '') && location.hostname == this.hostname) {
        var target = $(this.hash);
        target = target.length ? target : $('[name=' + this.hash.slice(1) + ']');
        if (target.length) {
          $('html, body').animate({
            scrollTop: (target.offset().top)
          }, 1000);
          // return false;
        }
      }
    });
  })
  // 選擇縣市時更新鄉鎮選單
  .on('change', '#city', function () {
    var city = $(this).val();
    var districts = cities[city].districts;
    $('#area').html('<option value="" selected>選擇區域</option>');
    for (var i in districts) {
      $('#area').append(`<option value="${districts[i]}">${districts[i]}</option>`);
    }
  })
  // search
  .on('click', '#gosearch', function (e) {
    e.preventDefault();
    var city = $('#city').val();
    var area = $('#area').val();
    var disease = $('#disease').val();
    var search_name = $('#search_name').val();
    if (city == "" && area == "" && disease == "" && search_name.length == 0) {
      return;
    }
    if (table) {
      table.destroy();
    }
    var answer = [];
    for (var i in hospitals) {
      var item = hospitals[i];
      // 有選擇時，物件內容是否符合
      if (city !== "" && item.city !== city) {
        continue;
      }
      if (area !== "" && item.town !== area) {
        continue;
      }
      if (disease == "all" && item.disease == "") {
        continue;
      }
      if (disease !== "" && disease !== "all" && (item.disease == "" || item.disease.indexOf(disease) == -1)) {
        continue;
      }
      if (search_name.length > 0 && item.name.indexOf(search_name) == -1) {
        continue;
      }
      // 判斷是否有推薦
      var recommend = item.recommend == 1 ? ' <span class="recommend">推薦名單</span>' : '';
      answer.push({
        name: item.name + recommend,
        phone: item.phone,
        address: item.address,
        info: item.info,
        recommend: item.recommend
      });
    }
    // 推薦排序提升
    answer.sort(function(a, b) {
      return a.recommend == 1 ? -1 : 1;
    });
    // 生成table
    showResultTable(answer);
  })

